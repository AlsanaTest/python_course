# Take two numbers as input
A = int(input("Enter first number: "))
B = int(input("Enter second number: "))

# Print sum
print("Sum:", A + B)

# Print difference
print("Difference:", A - B)

# Print product
print("Product:", A * B)

# Print quotient
print("Quotient:", A / B)

# Print remainder
print("Remainder:", A % B)
